#!/usr/bin/env python3
"""Generate the Phase-3 results section of EXPERIMENTS.md тАФ every figure sourced from the result
JSONs so the doc cannot drift from the data. Stdlib only (json).

Sources (committed):
  results/qa_{fixed_flat,fixed_hier,ca_flat,ca_hier}.json  тАФ accuracy, mean tiles/query, token usage
  results/qa_significance.json                              тАФ exact-McNemar contrasts (b, c, p)
  results/baseline_clean.json, results/content_aware.json   тАФ Phase-2 vision-token parity + DCG@10 Wilcoxon

Idempotent: replaces the block between the PHASE3 markers in EXPERIMENTS.md (appends if absent).
If a needed figure is missing from the JSONs it raises (KeyError) rather than emitting a guess.

    python research/gen_phase3_experiments.py            # rewrite the section in place
    python research/gen_phase3_experiments.py --print    # print the section, don't touch the file
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
RESULTS = ROOT / "results"
EXPERIMENTS = ROOT / "EXPERIMENTS.md"

# The ONLY non-JSON-sourced figures in this document: author-provided ILLUSTRATIVE list-price
# estimates (gpt-4o input pricing x projected tokens) for the full 4-cell sweep at each detail mode.
# NOT measured/logged тАФ included per explicit request and flagged as illustrative in the prose.
ILLUS_USD_DETAIL_HIGH = 37
ILLUS_USD_DETAIL_LOW = 4
BEGIN = "<!-- PHASE3:BEGIN (generated by research/gen_phase3_experiments.py тАФ do not hand-edit) -->"
END = "<!-- PHASE3:END -->"


def _load(name: str) -> dict:
    return json.loads((RESULTS / name).read_text())


def _f2(x: float) -> str:
    return f"{x:.2f}"


def build_section() -> str:
    cells = {
        "fixed_flat": _load("qa_fixed_flat.json"),
        "fixed_hier": _load("qa_fixed_hier.json"),
        "ca_flat": _load("qa_ca_flat.json"),
        "ca_hier": _load("qa_ca_hier.json"),
    }
    sig = _load("qa_significance.json")
    base = _load("baseline_clean.json")
    caj = _load("content_aware.json")
    smoke_hi = _load("qa_smoke.json")       # detail defaulted high (gpt-4o-mini probe)
    smoke_lo = _load("qa_smoke_low.json")   # detail=low (gpt-4o-mini probe)

    n = sig["n_queries"]
    con = {c["id"]: c for c in sig["contrasts"]}
    C1, C2, C3, C4 = con["C1"], con["C2"], con["C3"], con["C4"]

    # provenance for setup (read off the committed sweep config, not hand-typed)
    r = cells["fixed_flat"]["reader"]
    reader_model = r["model"]
    reader_detail = r["detail"]
    judge_model = cells["fixed_flat"]["judge_model"]
    cfg_flat = cells["fixed_flat"]["config"]
    cfg_hier = cells["ca_hier"]["config"]
    top_k = cfg_flat["reader_top_k"]
    nbr = cfg_hier["expand_neighbors"]
    cap = cfg_hier["expand_cap"]

    acc = sig["accuracies"]

    def p(x: float) -> str:
        return f"{x:.3f}"

    best_rel = C3["delta"] / C3["acc_a"] * 100.0

    # Phase-2 equal-budget parity + retrieval-significance companion (from the Phase-2 JSONs)
    vt_fixed = base["cost"]["vision_tokens_per_page"]["mean"]
    vt_ca = caj["cost"]["vision_tokens_per_page"]["mean"]
    parity_pct = (vt_ca - vt_fixed) / vt_fixed * 100.0
    k10 = caj["paired_vs_baseline"]["by_k"]["10"]["image"]
    wil = k10["wilcoxon"]
    dcg_p = wil["p_approx"]
    n_better, n_worse = k10["n_better"], k10["n_worse"]

    # committed token cost (measured; sum across the 4 cells)
    rd_prompt = {k: v["reader_usage"]["prompt_tokens"] for k, v in cells.items()}
    jd_prompt = {k: v["judge_usage"]["prompt_tokens"] for k, v in cells.items()}
    rd_total = sum(rd_prompt.values())
    jd_total = sum(jd_prompt.values())
    per_q_flat = rd_prompt["fixed_flat"] / n
    per_q_hier = rd_prompt["ca_hier"] / n

    # detail-mode probe тАФ measured on the gpt-4o-MINI smoke (NOT the gpt-4o sweep). The high/low
    # RATIO is the transferable finding; the absolute mini counts are not the sweep rate.
    def _smoke_pq(d: dict) -> float:
        m = d.get("n_graded") or len(d.get("per_query", []))
        return d["reader_usage"]["prompt_tokens"] / m

    smoke_n = smoke_hi.get("n_graded") or len(smoke_hi.get("per_query", []))
    smoke_model = smoke_hi["reader"]["model"]
    smoke_hi_pq = _smoke_pq(smoke_hi)
    smoke_lo_pq = _smoke_pq(smoke_lo)
    smoke_ratio = smoke_hi_pq / smoke_lo_pq

    # Caveat data (disambiguation experiment, research/CHUNKING_ANALYSIS.md): iNat image-query vs
    # text-query CA, sourced from results/qa_inat_text_*.json. Shows the headline is image-specific.
    t_ff = _load("qa_inat_text_fixed_flat.json")["accuracy"]
    t_fh = _load("qa_inat_text_fixed_hier.json")["accuracy"]
    t_cf = _load("qa_inat_text_ca_flat.json")["accuracy"]
    t_ch = _load("qa_inat_text_ca_hier.json")["accuracy"]
    ca_img_flat = acc["ca_flat"] - acc["fixed_flat"]
    ca_img_hier = acc["ca_hier"] - acc["fixed_hier"]
    ca_txt_flat = t_cf - t_ff
    ca_txt_hier = t_ch - t_fh

    def _saw_gold(cell_file: str, ret_json: dict) -> tuple[int, int]:
        """fraction of queries where the gold page reached the reader (gold_article_id in its tiles)."""
        gm = {q["qid"]: q.get("gold_article_id") for q in ret_json["per_query"]}
        pq = _load(cell_file)["per_query"]
        seen = sum(1 for rr in pq if gm.get(rr["qid"]) in {t[0] for t in rr.get("tiles", [])})
        return seen, len(pq)

    fx_seen, fx_nn = _saw_gold("qa_inat_text_fixed_flat.json", base)
    ca_seen, ca_nn = _saw_gold("qa_inat_text_ca_flat.json", caj)
    fx_txt_pct = round(100 * fx_seen / fx_nn)
    ca_txt_pct = round(100 * ca_seen / ca_nn)

    L = []
    A = L.append

    A("## Phase 3 тАФ hierarchical retrieve-then-expand: results")
    A("")
    A("End-to-end QA accuracy (the gating dependency for a publishable PixelRAG result; retrieval-only")
    A("metrics are not). The reader was deferred since Phase 1; this phase wires a pixel-native hosted")
    A("VLM reader + an automatic VLM-judge grader and runs the 2├Ч2 chunker├Чretrieval sweep.")
    A("")

    A("### Setup")
    A(f"**Corpus.** Same 40-page iNaturalist mini-corpus, **{n}** image-primary queries, image modality")
    A("(EVQA questions are deictic тАФ \"this plant\"). Reader and grader share the gold answers, so grading")
    A("needs no manual labeling.")
    A("")
    A(f"**Reader.** OpenAI `{reader_model}`, `detail={reader_detail}` тАФ pixel-native: retrieved chunk tiles")
    A("are passed as **images**, never parsed text. **Judge.** `" + judge_model + "` (VLM-as-judge, the")
    A("paper grader prompt + Correct/Incorrect/Unattempted parse reused from `eval/lib/grader.py`).")
    A("")
    A(f"**Retrieval modes.** `flat` = the top-{top_k} retrieved tiles. `hier-expand` = each retrieved tile")
    A(f"plus its same-section siblings and ┬▒{nbr} reading-order neighbors (deduped, capped at {cap}), via the")
    A("Phase-3 pageтЖТsectionтЖТtile tree. The fixed arm has no headings, so its hier-expand degrades to")
    A("reading-order-neighbors only (`expand_mode_effective=neighbors`) тАФ section expansion is the")
    A("content_aware-specific lever.")
    A("")
    A("**4-cell design.** `{fixed,content_aware}` ├Ч `{flat,hier-expand}`. The 2├Ч2 separates the **chunking**")
    A("effect (flat: fixed vs content_aware) from the **expansion** effect (flat vs hier-expand within an")
    A("arm) and exposes their **interaction**, so \"content_aware-hier beats fixed-flat\" is not confounded by")
    A("only one arm receiving expansion.")
    A("")

    A("### Results (4-cell, image-primary, n=" + str(n) + ")")
    A("| chunker | retrieval | accuracy | mean tiles/query |")
    A("|---|---|---|---|")
    A(f"| fixed | flat | {_f2(acc['fixed_flat'])} | {_f2(cells['fixed_flat']['mean_tiles_per_query'])} |")
    A(f"| fixed | hier-expand | {_f2(acc['fixed_hier'])} | {_f2(cells['fixed_hier']['mean_tiles_per_query'])} |")
    A(f"| content_aware | flat | {_f2(acc['ca_flat'])} | {_f2(cells['ca_flat']['mean_tiles_per_query'])} |")
    A(f"| content_aware | hier-expand | **{_f2(acc['ca_hier'])}** | {_f2(cells['ca_hier']['mean_tiles_per_query'])} |")
    A("")
    A(f"> **тЪая╕П Caveat тАФ this +{C3['delta']:.2f} headline is IMAGE-QUERY-SPECIFIC.** It does **not** survive a")
    A("> text-query modality flip on the *same* corpus and content_aware tiles (disambiguation experiment,")
    A(f"> `research/CHUNKING_ANALYSIS.md`). With text queries the content_aware advantage is "
      f"**{ca_txt_flat:+.2f} flat / {ca_txt_hier:+.2f} hier**")
    A(f"> (vs **{ca_img_flat:+.2f} flat / {ca_img_hier:+.2f} hier** with image queries). Mechanism: under image queries")
    A("> retrieval is saturated (the gold page reaches the reader ~96тАУ100% either way), so content_aware's gain")
    A(f"> is a **reader-side** effect of finer tiles; under text queries content_aware's finer tiles **reduce**")
    A(f"> gold-page retrieval **{fx_txt_pct}%тЖТ{ca_txt_pct}%**, and the advantage vanishes. So content-aware chunking")
    A("> does **not** improve QA on its own тАФ the gain here is contingent on image-query retrieval.")
    A("")

    A("### Decomposition")
    A(f"- **Chunking** (fixed-flat тЖТ content_aware-flat): {_f2(acc['fixed_flat'])} тЖТ {_f2(acc['ca_flat'])} "
      f"(**{C1['delta']:+.2f}**) тАФ better boundaries help even with no expansion.")
    A(f"- **Expansion** (content_aware-flat тЖТ content_aware-hier): {_f2(acc['ca_flat'])} тЖТ {_f2(acc['ca_hier'])} "
      f"(**{C2['delta']:+.2f}**) тАФ adding section + reading-order context to the same retrieved tiles.")
    A(f"- **Best vs baseline** (fixed-flat тЖТ content_aware-hier): {_f2(acc['fixed_flat'])} тЖТ {_f2(acc['ca_hier'])} "
      f"(**{C3['delta']:+.2f}**, тЙИ{best_rel:.0f}% relative).")
    A(f"- **Synergy, not addition.** Expansion on the **fixed** arm does **not** help "
      f"({_f2(acc['fixed_flat'])} тЖТ {_f2(acc['fixed_hier'])}, **{C4['delta']:+.2f}**): reading-order neighbors")
    A("  around fixed-height cuts add noise as often as signal. The same expansion machinery only pays off")
    A("  once chunk boundaries follow content. The fixed-hier cell is the **control** that establishes this тАФ")
    A("  the expansion gain is contingent on content-aware boundaries; the two techniques are synergistic.")
    A("")

    A(f"### Significance тАФ exact two-sided McNemar (binomial, paired, n={n})")
    A("Per query: 1 iff the judge verdict is `correct` (incorrect and unattempted both 0). `b` = baseline-arm")
    A("correct / other-arm wrong; `c` = the reverse. Exact binomial only тАФ n is too small for the chi-square")
    A("or normal approximation (`research/qa_significance.py`).")
    A("")
    A("| contrast | acc | ╬Фacc | b (aтЬУ/bтЬЧ) | c (aтЬЧ/bтЬУ) | p (exact) |")
    A("|---|---|---|---|---|---|")
    A(f"| C1 chunking (fixed-flat тЖТ ca-flat) | {_f2(C1['acc_a'])}тЖТ{_f2(C1['acc_b'])} | {C1['delta']:+.2f} | "
      f"{C1['b_a_correct_b_wrong']} | {C1['c_a_wrong_b_correct']} | {p(C1['p_exact_mcnemar'])} |")
    A(f"| C2 expansion (ca-flat тЖТ ca-hier) | {_f2(C2['acc_a'])}тЖТ{_f2(C2['acc_b'])} | {C2['delta']:+.2f} | "
      f"{C2['b_a_correct_b_wrong']} | {C2['c_a_wrong_b_correct']} | {p(C2['p_exact_mcnemar'])} |")
    A(f"| C3 full (fixed-flat тЖТ ca-hier) | {_f2(C3['acc_a'])}тЖТ{_f2(C3['acc_b'])} | {C3['delta']:+.2f} | "
      f"{C3['b_a_correct_b_wrong']} | {C3['c_a_wrong_b_correct']} | **{p(C3['p_exact_mcnemar'])}** |")
    A(f"| C4 expansion-on-fixed (fixed-flat тЖТ fixed-hier) | {_f2(C4['acc_a'])}тЖТ{_f2(C4['acc_b'])} | {C4['delta']:+.2f} | "
      f"{C4['b_a_correct_b_wrong']} | {C4['c_a_wrong_b_correct']} | {p(C4['p_exact_mcnemar'])} |")
    A("")
    A(f"- **Full method** ({_f2(C3['acc_a'])} тЖТ {_f2(C3['acc_b'])}, {C3['delta']:+.2f}) is significant "
      f"(**p={p(C3['p_exact_mcnemar'])}**); all {C3['c_a_wrong_b_correct']} discordant pairs favor the full method "
      f"(b={C3['b_a_correct_b_wrong']}).")
    A(f"- **Components are individually monotone** тАФ no query regresses under either step "
      f"(b={C1['b_a_correct_b_wrong']} for C1, b={C2['b_a_correct_b_wrong']} for C2) тАФ but neither clears "
      f"significance alone at this n (p={p(C1['p_exact_mcnemar'])} each). This is what motivates the Phase-4 larger sweep.")
    A(f"- **Expansion on fixed chunks** shows no improvement ({C4['delta']:+.2f}, p={p(C4['p_exact_mcnemar'])}) тЖТ the gain")
    A("  is contingent on content-aware boundaries.")
    A("")
    A(f"The **strict monotonicity** тАФ zero regressions (b=0) across C1, C2, **and** C3 тАФ is the key structural")
    A("evidence at this pilot scale: every contrast moves one way only. The better-powered companion sits one")
    A(f"level down, at retrieval: the Phase-2 paired Wilcoxon on per-query DCG@10 (p={dcg_p}, {n_better}/{n_worse}")
    A("better/worse) is what the QA numbers rest on.")
    A("")

    A("### Cost")
    A(f"**Equal vision-token budget.** content_aware and fixed are compared at matched reader budget: mean")
    A(f"vision-tokens/page **{vt_fixed:.0f}** (fixed) vs **{vt_ca:.0f}** (content_aware), **{parity_pct:+.1f}%** "
      f"(Phase 2). The")
    A("QA gain is therefore not bought with more reader tokens тАФ content_aware uses more, smaller,")
    A("content-aligned chunks at the same total pixels.")
    A("")
    A(f"**Measured sweep cost (this run).** Reader at `detail={reader_detail}`: ~{per_q_flat:.0f} prompt tokens/query")
    A(f"at {top_k} flat tiles, ~{per_q_hier:.0f} at hier-expand; **{rd_total:,}** reader prompt tokens total across the")
    A(f"4 cells, plus **{jd_total:,}** judge prompt tokens (text-only VLM-judge). Per-cell token usage is recorded in")
    A("each `results/qa_*.json`.")
    A("")
    A("**Detail-mode token fix.** OpenAI defaults image inputs to `detail=high`, which tiles a tall full-page")
    A(f"screenshot into many crops. A {smoke_n}-query smoke probe on **`{smoke_model}`** (a per-image-detail")
    A(f"probe тАФ *not* the sweep model) measured **{smoke_hi_pq:,.0f}** prompt tokens/query at `detail=high` vs")
    A(f"**{smoke_lo_pq:,.0f}** at `detail=low` тАФ a тЙИ{smoke_ratio:.0f}├Ч ({smoke_ratio:.1f}├Ч) reduction. The **ratio** is")
    A("the transferable finding; the absolute mini-smoke counts are not the gpt-4o sweep rate (the committed")
    A(f"totals above report the gpt-4o sweep at ~{per_q_flat:.0f} flat / ~{per_q_hier:.0f} hier tokens/query). At")
    A(f"list prices this is the difference between an illustrative тЙИ${ILLUS_USD_DETAIL_HIGH} (`detail=high`) and")
    A(f"тЙИ${ILLUS_USD_DETAIL_LOW} (`detail=low`) full sweep (gpt-4o input pricing ├Ч projected tokens тАФ illustrative,")
    A("**not** a measured/logged cost). Answers were observed identical to `detail=high` on the smoke (a manual")
    A("spot-check, not a logged metric).")
    A("")

    A("### Built vs. not built")
    A("- **Built.** Hierarchical retrieve-then-expand: a retrieved tile тЖТ same-section siblings + "
      f"┬▒{nbr} reading-order neighbors (deduped, capped), over a pageтЖТsectionтЖТtile tree derived from the")
    A("  chunker's existing metadata. No re-embedding, no re-render.")
    A("- **Not built (future work).** Cross-reference / dependency-graph tracing тАФ following anchors like")
    A("  \"see Table 3\" or DOM internal links to pull in referenced regions. No claim of full dependency")
    A("  tracing or complete context is made.")
    A("")

    A("### Limitations (honest)")
    A(f"- **Pilot scale.** n={n} queries, single corpus; QA significance is limited by n (the C1/C2 "
      f"p={p(C1['p_exact_mcnemar'])}). The retrieval-level result is better powered; a larger QA sweep is Phase 4.")
    A("- **Single configuration.** Image modality only; one hosted reader (gpt-4o); judge grading on short")
    A("  descriptive answers.")
    A("")

    A("### Positioning")
    A("A controlled **port + ablation + efficiency study**, not a new method. Content-aware visual chunking")
    A("and retrieval hierarchy already exist (M3DocDep; ColChunk / visual late chunking; MHier-RAG), and")
    A("PixelRAG established pixel-native RAG. The contribution here is a cost-neutral ablation showing that")
    A("content-aware chunking and section-expansion each contribute and are **synergistic**, and that the")
    A("Phase-2 retrieval gain **translates end-to-end to QA**.")
    A("")

    A("### Reproduce")
    A("Per cell: `python qa_eval.py --results results/<arm>.json --tiles-dir <tiles> --gold")
    A("research/mini_corpus/gold.jsonl --retrieval {flat,hier-expand} --reader openai --reader-detail low`")
    A("тЖТ `results/qa_<chunker>_<retrieval>.json`. Significance: `python research/qa_significance.py` тЖТ")
    A("`results/qa_significance.json`. This section: `python research/gen_phase3_experiments.py`.")

    return "\n".join(L)


def main() -> int:
    section = build_section()
    block = f"{BEGIN}\n{section}\n{END}\n"
    if "--print" in sys.argv:
        print(section)
        return 0

    text = EXPERIMENTS.read_text()
    if BEGIN in text and END in text:
        pre = text[: text.index(BEGIN)]
        post = text[text.index(END) + len(END):]
        new = pre + block + post.lstrip("\n")
    else:
        new = text.rstrip("\n") + "\n\n---\n\n" + block
    EXPERIMENTS.write_text(new)
    print(f"updated {EXPERIMENTS} (Phase-3 section, {len(section.splitlines())} lines)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
